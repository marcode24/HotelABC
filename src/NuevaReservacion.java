    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:
        Validaciones validacion = new Validaciones();
        validacion.setDias(LocalDate.now(), dpSalida.getDate());
        if (validacion.validarFechas()) {
            JOptionPane.showMessageDialog(null, "Ingrese una fecha de Salida Correcta", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            salidaTemp = dpSalida.getDate();
            HabitacionController habitacionC = new HabitacionController();
            ArrayList<String> listHabitaciones = habitacionC.checarDisponiblidad(dpSalida.getDate());
            
            Set<String> set = new HashSet<>(listHabitaciones);
            listHabitaciones.clear();
            listHabitaciones.addAll(set);
            
            habitaciones = habitacionC.obtenerHabitaciones();
            
            if(listHabitaciones.size() > 0) {
                String array2[] = new String[listHabitaciones.size()];
                for(int i = 0; i < array2.length ; i++){
                    String nombre2 = listHabitaciones.get(i);
                    Iterator<Habitacion> it = habitaciones.iterator();
                    while(it.hasNext()) {
                        if(it.next().getNombre().equals(nombre2)) {
                            it.remove();
                        }   
                    }
                }
            }
            if(habitaciones.size() > 0) {
                noches = validacion.obtenerNoches();
                panelHabitacion.setVisible(true);
                cargarHabitaciones();
            } else {
                JOptionPane.showMessageDialog(null, "No hay habitaciones disponibles", "Error", JOptionPane.ERROR_MESSAGE);
                panelHabitacion.setVisible(false);
            }
        }
        
    }//GEN-LAST:event_btnVerificarActionPerformed

    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        
        Horario nHorario = new Horario();
        nHorario.setEstado("Activo");
        nHorario.setIdHabitacion(idHabitacion);
        nHorario.setLlegada(LocalDate.now());
        nHorario.setSalida(salidaTemp);
        nHorario.setNoches(Integer.parseInt(noches.toString()));
        nHorario.setTotal(total);
        // agregar el total ""pendiente""
        
        HorarioController horarioC = new HorarioController();
        HabitacionController habitacionC = new HabitacionController();
        if(horarioC.crearHorarioHabitacion(nHorario) && habitacionC.cambiarDisponibilidad(idHabitacion)) {
            resetearCampos();
            JOptionPane.showMessageDialog(null, "Habitaci√≥n reservada, disfrute su estancia", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrio un error intentelo de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnReservarActionPerformed


